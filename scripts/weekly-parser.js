// 각 주차별 파일 생성
function createWeeklyFiles() {
    Object.keys(weeklyData).forEach(week => {
        const data = weeklyData[week];
        const fileName = `date/${week}.txt`;
        
        let content = `📅 ${week} (${data.period})\n`;
        content += `${'='.repeat(50)}\n\n`;
        
        content += `📋 주요 작업 목록:\n`;
        data.tasks.forEach((task, index) => {
            content += `${index + 1}. ${task}\n`;
        });
        
        content += `\n📊 주간 요약:\n`;
        content += `• 총 작업 수: ${data.tasks.length}개\n`;
        content += `• 기간: ${data.period}\n`;
        
        // 팀별 작업 수 계산
        const teamCounts = {};
        data.tasks.forEach(task => {
            const team = task.split(' - ')[0];
            teamCounts[team] = (teamCounts[team] || 0) + 1;
        });
        
        content += `\n👥 팀별 작업 분포:\n`;
        Object.keys(teamCounts).forEach(team => {
            content += `• ${team}: ${teamCounts[team]}개 작업\n`;
        });
        
        fs.writeFileSync(fileName, content, 'utf8');
        console.log(`✅ ${fileName} 파일이 생성되었습니다.`);
    });
}

// 실행
